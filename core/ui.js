export function confirmDialog(options = {}) {
  const {
    title = '确认操作',
    message = '',
    confirmText = '确认',
    cancelText = '取消',
    danger = false
  } = options;

  return new Promise((resolve) => {
    const cancelButton = button({
      text: cancelText,
      className: 'secondary-button',
      onClick: () => {
        instance.close();
        resolve(false);
      }
    });

    const okButton = button({
      text: confirmText,
      className: danger ? 'danger-button' : 'primary-button',
      onClick: () => {
        instance.close();
        resolve(true);
      }
    });

    const instance = modal({
      title,
      content: message,
      actions: [cancelButton, okButton]
    });
  });
}

export function promptDialog(options = {}) {
  const {
    title = '输入内容',
    label = '',
    value = '',
    placeholder = '',
    multiline = false,
    confirmText = '保存',
    cancelText = '取消'
  } = options;

  return new Promise((resolve) => {
    const field = multiline
      ? textareaField({ label, value, placeholder })
      : inputField({ label, value, placeholder });

    const input = field.input || field.textarea;

    const cancelButton = button({
      text: cancelText,
      className: 'secondary-button',
      onClick: () => {
        instance.close();
        resolve(null);
      }
    });

    const okButton = button({
      text: confirmText,
      className: 'primary-button',
      onClick: () => {
        instance.close();
        resolve(input.value);
      }
    });

    const instance = modal({
      title,
      content: field.wrap,
      actions: [cancelButton, okButton]
    });

    window.setTimeout(() => input.focus(), 80);
  });
}

