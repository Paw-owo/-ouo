export function debounce(fn, delay = 200) {
  let timer = null;

  return (...args) => {
    window.clearTimeout(timer);
    timer = window.setTimeout(() => fn(...args), delay);
  };
}

export function throttle(fn, delay = 200) {
  let locked = false;

  return (...args) => {
    if (locked) {
      return;
    }

    locked = true;
    fn(...args);

    window.setTimeout(() => {
      locked = false;
    }, delay);
  };
}

export function delegate(parent, selector, eventName, handler) {
  const listener = (event) => {
    const target = event.target.closest(selector);

    if (target && parent.contains(target)) {
      handler(event, target);
    }
  };

  parent.addEventListener(eventName, listener);

  return () => {
    parent.removeEventListener(eventName, listener);
  };
}

export function longPress(element, callback, delay = 520) {
  let timer = null;
  let startX = 0;
  let startY = 0;

  const clear = () => {
    window.clearTimeout(timer);
    timer = null;
  };

  const start = (event) => {
    const point = event.touches ? event.touches[0] : event;
    startX = point.clientX;
    startY = point.clientY;

    timer = window.setTimeout(() => {
      callback(event);
      clear();
    }, delay);
  };

  const move = (event) => {
    const point = event.touches ? event.touches[0] : event;

    if (Math.abs(point.clientX - startX) > 10 || Math.abs(point.clientY - startY) > 10) {
      clear();
    }
  };

  element.addEventListener('mousedown', start);
  element.addEventListener('touchstart', start, { passive: true });
  element.addEventListener('mousemove', move);
  element.addEventListener('touchmove', move, { passive: true });
  element.addEventListener('mouseup', clear);
  element.addEventListener('mouseleave', clear);
  element.addEventListener('touchend', clear);
  element.addEventListener('touchcancel', clear);

  return () => {
    clear();
    element.removeEventListener('mousedown', start);
    element.removeEventListener('touchstart', start);
    element.removeEventListener('mousemove', move);
    element.removeEventListener('touchmove', move);
    element.removeEventListener('mouseup', clear);
    element.removeEventListener('mouseleave', clear);
    element.removeEventListener('touchend', clear);
    element.removeEventListener('touchcancel', clear);
  };
}

export function createSwipe(element, options = {}) {
  const {
    onLeft = null,
    onRight = null,
    threshold = 48
  } = options;

  let startX = 0;
  let startY = 0;

  const start = (event) => {
    const point = event.touches ? event.touches[0] : event;
    startX = point.clientX;
    startY = point.clientY;
  };

  const end = (event) => {
    const point = event.changedTouches ? event.changedTouches[0] : event;
    const dx = point.clientX - startX;
    const dy = point.clientY - startY;

    if (Math.abs(dx) < threshold || Math.abs(dx) < Math.abs(dy)) {
      return;
    }

    if (dx < 0 && typeof onLeft === 'function') {
      onLeft(event);
    }

    if (dx > 0 && typeof onRight === 'function') {
      onRight(event);
    }
  };

  element.addEventListener('touchstart', start, { passive: true });
  element.addEventListener('touchend', end, { passive: true });
  element.addEventListener('mousedown', start);
  element.addEventListener('mouseup', end);

  return () => {
    element.removeEventListener('touchstart', start);
    element.removeEventListener('touchend', end);
    element.removeEventListener('mousedown', start);
    element.removeEventListener('mouseup', end);
  };
}

