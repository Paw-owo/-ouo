export function createTabs(tabs = [], activeId = '', onChange = null) {
  const wrap = h('div', { className: 'tabs' });

  function setActive(nextId) {
    qsa('.tab-button', wrap).forEach((tab) => {
      tab.classList.toggle('active', tab.dataset.tabId === nextId);
    });
  }

  tabs.forEach((tab) => {
    const tabButton = h('button', {
      type: 'button',
      className: ['tab-button', tab.id === activeId ? 'active' : ''].join(' '),
      text: tab.name || tab.label || '',
      dataset: { tabId: tab.id },
      onClick: () => {
        setActive(tab.id);

        if (typeof onChange === 'function') {
          onChange(tab.id, tab);
        }
      }
    });

    wrap.appendChild(tabButton);
  });

  return wrap;
}

