export function qs(selector, parent = document) {
  return parent.querySelector(selector);
}

export function qsa(selector, parent = document) {
  return Array.from(parent.querySelectorAll(selector));
}

export function escapeHTML(value = '') {
  return String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');
}

export function createElement(tag = 'div', props = {}, children = []) {
  const element = document.createElement(tag);
  const childList = Array.isArray(children) ? children : [children];

  Object.entries(props || {}).forEach(([key, value]) => {
    if (value === undefined || value === null || value === false) {
      return;
    }

    if (key === 'class' || key === 'className') {
      element.className = Array.isArray(value) ? value.filter(Boolean).join(' ') : String(value);
      return;
    }

    if (key === 'text') {
      element.textContent = String(value);
      return;
    }

    if (key === 'html') {
      element.innerHTML = String(value);
      return;
    }

    if (key === 'dataset') {
      Object.entries(value).forEach(([dataKey, dataValue]) => {
        if (dataValue !== undefined && dataValue !== null) {
          element.dataset[dataKey] = String(dataValue);
        }
      });
      return;
    }

    if (key === 'style') {
      Object.entries(value).forEach(([styleKey, styleValue]) => {
        if (styleValue !== undefined && styleValue !== null) {
          element.style[styleKey] = styleValue;
        }
      });
      return;
    }

    if (key.startsWith('on') && typeof value === 'function') {
      element.addEventListener(key.slice(2).toLowerCase(), value);
      return;
    }

    if (key in element) {
      element[key] = value;
      return;
    }

    element.setAttribute(key, String(value));
  });

  childList.flat().forEach((child) => {
    if (child === undefined || child === null || child === false) {
      return;
    }

    if (child instanceof Node) {
      element.appendChild(child);
    } else {
      element.appendChild(document.createTextNode(String(child)));
    }
  });

  return element;
}

export const h = createElement;

export function clearElement(element) {
  if (element) {
    element.replaceChildren();
  }

  return element;
}

export function render(element, children = []) {
  clearElement(element);
  const childList = Array.isArray(children) ? children : [children];

  childList.forEach((child) => {
    if (child instanceof Node) {
      element.appendChild(child);
    }
  });

  return element;
}

export function icon(name = 'empty', className = '') {
  const svg = document.createElementNS('http://www.w3.org/2000/svg', 'svg');

  svg.setAttribute('viewBox', '0 0 24 24');
  svg.setAttribute('aria-hidden', 'true');

  if (className) {
    svg.setAttribute('class', className);
  }

  svg.innerHTML = ICONS[name] || ICONS.empty;
  return svg;
}

export function button(options = {}) {
  const {
    text = '',
    iconName = '',
    className = 'text-button',
    title = text,
    onClick = null,
    type = 'button',
    dataset = {}
  } = options;

  const children = [];

  if (iconName) {
    children.push(icon(iconName));
  }

  if (text) {
    children.push(h('span', { text }));
  }

  return h('button', {
    type,
    title,
    className,
    dataset,
    onClick
  }, children);
}

export function iconButton(iconName, options = {}) {
  return button({
    ...options,
    iconName,
    text: options.text || '',
    className: options.className || 'icon-button',
    title: options.title || options.text || ''
  });
}

