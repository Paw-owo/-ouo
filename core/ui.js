export function closeLayer(layer) {
  if (!layer) {
    return;
  }

  layer.classList.remove('active');

  window.setTimeout(() => {
    layer.remove();
  }, 220);
}

export function modal(options = {}) {
  const {
    title = '',
    content = null,
    actions = null,
    closeOnBackdrop = true
  } = options;

  const layer = createLayer('modal-layer');
  const modalCard = h('div', { className: 'modal-card' });
  const header = title ? h('div', { className: 'card-title', text: title }) : null;
  const body = h('div', { className: 'card-text' });

  if (content instanceof Node) {
    body.appendChild(content);
  } else if (content) {
    body.textContent = String(content);
  }

  if (header) {
    modalCard.appendChild(header);
  }

  modalCard.appendChild(body);

  if (actions) {
    modalCard.appendChild(h('div', { className: 'card-actions' }, actions));
  }

  layer.appendChild(modalCard);

  if (closeOnBackdrop) {
    layer.addEventListener('click', (event) => {
      if (event.target === layer) {
        closeLayer(layer);
      }
    });
  }

  return {
    layer,
    card: modalCard,
    close: () => closeLayer(layer)
  };
}

